    private void loadMovimentacoes() {

        // Abre a conexão
        jpa.conexaoAberta();
        try {

            // Busca as EntradaSaida cadastradas
            List<EntradaSaida> listaMovimentacoes = jpa.getMovimentacoes(null); // Método criado no DAO

            // Obtém o modelo da tabela
            DefaultTableModel modeloTabela = (DefaultTableModel) tblMovimentacoes.getModel();

            // Limpa a tabela 
            modeloTabela.setRowCount(0);
            System.out.println("Movimentações: " + listaMovimentacoes);

            // Adiciona os veículos ao modelo da tabela
            if (listaMovimentacoes != null && !listaMovimentacoes.isEmpty()) {
                // Adiciona as movimentações ao modelo da tabela
                for (EntradaSaida mov : listaMovimentacoes) {
                    Object[] linha = {
                        mov.getTipoMovimentacao(),
                        formatoData.format(mov.getData()),
                        mov // Adiciona o objeto completo
                    };
                    modeloTabela.addRow(linha);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao carregar as EntradaSaida: " + e.getMessage());
        } finally {
            // Fecha a conexão
            jpa.fecharConexao();
        }
    }

    private EntradaSaida getMovimentacaoSel() {
        int selectedRow = tblMovimentacoes.getSelectedRow();
        if (selectedRow != -1) {
            EntradaSaida movimentacao = (EntradaSaida) tblMovimentacoes.getValueAt(selectedRow, 2);
            return movimentacao;
        } else {
            JOptionPane.showMessageDialog(TelaMovimentacoesDia.this, "Não há movimentação selecionada.");
            return null;
        }
    }

