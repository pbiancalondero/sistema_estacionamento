    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        EntradaSaida movimentacaoSelecionada = getMovimentacaoSel();
        if (movimentacaoSelecionada != null) {
            int delOp = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover movimentação " + movimentacaoSelecionada + "?");
            if (delOp == JOptionPane.YES_OPTION) {
                try {
                    jpa.conexaoAberta();

                    jpa.remover(movimentacaoSelecionada);
                    JOptionPane.showMessageDialog(rootPane, "Movimentação removida com sucesso!");

                    loadMovimentacoes(); // Atualiza a tabela
                } catch (Exception e) {
                    e.printStackTrace();
                    System.err.println("Erro ao remover movimentação " + movimentacaoSelecionada + "\nErro: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }

            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnSaidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaidaActionPerformed
        EntradaSaida movimentacaoSelecionada = getMovimentacaoSel();
        if (movimentacaoSelecionada != null) {
            int delOp = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja informar saída do veículo " + movimentacaoSelecionada + "?");
            if (delOp == JOptionPane.YES_OPTION) {
                try {
                    jpa.conexaoAberta();
                    EntradaSaida movimentacao = new EntradaSaida(TipoMovimentacao.SAIDA, movimentacaoSelecionada.getVeiculo());
                    movimentacao.setData(new Date()); // Aqui definimos a data do sistema
                    jpa.persist(movimentacao);
                    JOptionPane.showMessageDialog(rootPane, "Saída registrada com sucesso!");

                    loadMovimentacoes(); // Atualiza a tabela
                } catch (Exception e) {
                    e.printStackTrace();
                    System.err.println("Erro ao registrar movimentação " + movimentacaoSelecionada + "\nErro: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }

            }
        }
    }//GEN-LAST:event_btnSaidaActionPerformed

